package com.example.demo.entity;

import javax.persistence.Column;
import javax.persistence.Table;

@Table(name="person")
public class Person {
    private String name;
    private int age;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}

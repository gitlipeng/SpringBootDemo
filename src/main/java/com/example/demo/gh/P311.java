package com.example.demo.gh;

public class P311 {
    private int i;
    private int j;
    private double n;

    public P311(int i, int j, double n) {
        this.i = i;
        this.j = j;
        this.n = n;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getJ() {
        return j;
    }

    public void setJ(int j) {
        this.j = j;
    }

    public double getN() {
        return n;
    }

    public void setN(double n) {
        this.n = n;
    }
}
